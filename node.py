class Node:
    def __init__(self, value):
        self.val = value
        self.next = None

class DoubleNode:
    def __init__(self, value):
        self.val = value
        self.next = None
        self.prev = None

class TreeNode:
    def __init__(self, value):
        self.val = value
        self.left = None
        self.right = None
